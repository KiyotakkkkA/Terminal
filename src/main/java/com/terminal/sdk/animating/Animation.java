package com.terminal.sdk.animating;

/**
 * Интерфейс для создания анимаций в консоли.
 * Определяет базовые методы для управления анимацией.
 */
public interface Animation {
    /**
     * Возвращает текущий кадр анимации.
     */
    String getCurrentFrame();
    
    /**
     * Обновляет состояние анимации.
     */
    void update();
    
    /**
     * Возвращает задержку между кадрами в миллисекундах.
     */
    int getFrameDelay();
    
    /**
     * Проверяет, завершена ли анимация.
     */
    boolean isFinished();
    
    /**
     * Запускает анимацию.
     */
    void start();
    
    /**
     * Останавливает анимацию.
     */
    void stop();
    
    /**
     * Сбрасывает анимацию в начальное состояние.
     */
    void reset();
} 